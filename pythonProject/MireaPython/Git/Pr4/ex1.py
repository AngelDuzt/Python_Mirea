def main(dictionary):
    z1 = int(dictionary['z1']) & 0b0000000000000000000000000000000111
    z2 = int(dictionary['z2']) << 3 & 0b0000000000000000000000000111111000
    z3 = int(dictionary['z3']) << 9 & 0b0000000000000000000111111000000000
    z5 = int(dictionary['z5']) << 17 & 0b0000000000011111100000000000000000
    z6 = int(dictionary['z6']) << 23 & 0b1111111111100000000000000000000000
    return(z1 | z2 | z3 | z5 | z6)

